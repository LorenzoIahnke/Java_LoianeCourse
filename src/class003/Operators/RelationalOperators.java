package class003.Operators;

public class RelationalOperators {

	public static void main(String[] args) {
		int var1 = 1;
		int var2 = 2;
		
		System.out.println("var1 == var2: " + (var1 == var2));
		System.out.println("var1 != var2: " + (var1 != var2));
		System.out.println("var1 > var2: " + (var1 > var2));
		System.out.println("var1 >= var2: " + (var1 >= var2));
		System.out.println("var1 < var2: " + (var1 < var2));
		System.out.println("var1 <= var2: " + (var1 <= var2));
	}

}

package class023.Exercise004;

public class ConvertingVolumeUnits {
	public static double litersToCentimeters(double num) {
		return num*1000;
	}
	public static double metersToLiters(double num) {
		return num*1000;
	}
	public static double metersToFeet(double num) {
		return num*35.32;
	}
	public static double galonToInch(double num) {
		return num*231;
	}
	public static double galonToLiter(double num) {
		return num*3.785;
	}
}

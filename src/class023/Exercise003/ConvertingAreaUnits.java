package class023.Exercise003;

public class ConvertingAreaUnits {
	
	public static double metersToFeet(double num) {
		return num*10.76;
	}
	public static double feetToCentimeters(double num) {
		return num*929;
	}
	public static double milesToAcres(double num) {
		return num*640;
	}
	public static double acresToFeet(double num) {
		return num*43560;
	}
}
